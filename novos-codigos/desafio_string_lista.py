# Variável que armazena uma string
mensagem = "Estudos de lógica com Python"

# Lista que armazena várias strings
linguagens = ["Python", "JavaScript", "Java", "C#"]

# Exibe a string
print(mensagem)

# Percorre e exibe a lista
print("\nLinguagens armazenadas na lista:")
for linguagem in linguagens:
    print(f"- {linguagem}")
